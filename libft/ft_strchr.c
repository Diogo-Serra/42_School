#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	const char	ch = (char)c;
	size_t		i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == ch)
			return ((char *)&s[i]);
		i++;
	}
	if (ch == '\0')
		return ((char *)&s[i]);
	return (NULL);
}
