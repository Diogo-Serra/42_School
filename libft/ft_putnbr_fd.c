#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	arr[11];
	long	i[2];

	i[0] = 10;
	i[1] = (long)n;
	if (i[1] < 0)
		i[1] *= -1;
	if (i[1] == 0)
		arr[--i[0]] = '0';
	while (i[1] > 0)
	{
		arr[--i[0]] = (i[1] % 10) + '0';
		i[1] /= 10;
	}
	if (n < 0)
		arr[--i[0]] = '-';
	while (i[0] < 10)
		write(fd, &arr[i[0]++], 1);
}
