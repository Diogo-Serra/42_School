#include "libft.h"

int	ft_atoi(const char *s)
{
	int	nb;
	int	sg;

	nb = 0;
	sg = 1;
	while ((*s == 32) || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s++ == '-')
			sg *= -1;
	}
	while (*s >= '0' && *s <= '9')
		nb = (nb * 10) + (*s++ - '0');
	return (sg * nb);
}
