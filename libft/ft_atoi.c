#include "libft.h"

int	ft_atoi(const char *s)
{
	size_t	i;
	long	res;
	int		sign;

	sign = 1;
	res = 0;
	i = 0;
	while ((s[i] == ' ') || (s[i] >= 9 && s[i] <= 13))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = (res * 10) + (s[i] - '0');
		i++;
	}
	return ((int)(res * sign));
}
