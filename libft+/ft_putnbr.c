#include "libft.h"

void	ft_putnbr(int n)
{
	long	nb;
	char	c;

	nb = n;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr((int)(nb / 10));
	c = (nb % 10) + '0';
	ft_putchar(c);
}
